package by.mksn.epam.bidbuy.pool.exception;

public class WrapperConnectionException extends Exception {

    public WrapperConnectionException() {
        super();
    }

    public WrapperConnectionException(String cause) {
        super(cause);
    }

    public WrapperConnectionException(Throwable t) {
        super(t);
    }

    public WrapperConnectionException(String cause, Throwable t) {
        super(cause, t);
    }

}
